__title__ = "Compound htz"
__description__ = "Identify compound heterozygous variants"
__long_description__ = """Compound heterozygosity means a particular gene has two different mutated alleles. 

This plugin allows to:
- automatically create a filter keeping only such variants (based on current source)
- automatically apply a tag to those selected variants

This tag can be used down the line to identify and select those variants. """
